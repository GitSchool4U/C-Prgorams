int main()
{
    // how many times OUTPUT-1 will be run
    for (;;)
    {
        printf("OUTPUT-1\n");
    }

    // how many times OUTPUT-2 will be run
    while (0)
    {
        printf("OUTPUT-2\n");
    }

    // how many times OUTPUT-3 will be run
    do
    {
        printf("OUTPUT-3\n");
    } while (0);

    return 0;
}
